
public class Gugudan {

	public static void main(String[] args) {

		int i = 1;

		while (i < 10) {
			System.out.println(6 *i);
			i++;
		}

		for (i = 1; i < 10; i++) {
			System.out.println(6 * i);
		}

		while (i < 10) {
			System.out.println(7 * i);
			i++;
		}

		for (i = 1; i < 10; i++) {
			System.out.println(7 * i);
		}
	}

}
